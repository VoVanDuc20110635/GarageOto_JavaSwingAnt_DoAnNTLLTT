    private void btn_capNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_capNhatActionPerformed
        ChiTietPhieuNhapHang chiTietPhieuNhapHangUpdate = new ChiTietPhieuNhapHang();
        chiTietPhieuNhapHangUpdate.setMaChiTietPhieuNhapHang(lb_maChiTietDonNhapHang.getText());
        chiTietPhieuNhapHangUpdate.setGiam_gia(Double.parseDouble(tf_giamGia.getText()));
        chiTietPhieuNhapHangUpdate.setSo_luong(Short.parseShort(tf_soLuong.getText()));
        chiTietPhieuNhapHangUpdate.setGia_nhap(Double.parseDouble(tf_giaNhap.getText()));
        try {
            chiTietPhieuNhapHangService.updateChiTietPhieuNhapHang(chiTietPhieuNhapHangUpdate);
        } catch (SQLException ex) {
            Logger.getLogger(Frame_CapNhatChiTietDonNhapHang.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_btn_capNhatActionPerformed

